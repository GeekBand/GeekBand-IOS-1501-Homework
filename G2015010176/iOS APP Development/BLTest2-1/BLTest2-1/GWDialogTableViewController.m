- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    UITableViewCell *myCell = [tableView dequeueReusableCellWithIdentifier:@"myMessageCellIdentifier"];
    
    UITableViewCell *friendsCell = [tableView dequeueReusableCellWithIdentifier:@"friendsMessageCellIdentifier"];
    
    // Configure the cell...
    
