- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Song Lists Reuse Cell" forIndexPath:indexPath];
    
    cell.imageView.image      = [UIImage imageNamed:@"artwork_yishengsuoai"];
    cell.textLabel.text       = @"一生所爱";
    cell.detailTextLabel.text = @"卢冠廷 - 《大话西游》插曲";
    
    
    return cell;
}


- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    
    if (indexPath.row == 0) {
        
        [[GWMusicPlayer sharedInstance] playOrPause];
    }
    
}

